# 字符串切片操作
a = "123456"
print(a[1])

print(a[1:3])

print(a[-1])

print(a[-3: -1])

print(a[-2:])

print(a[:-1])

print("this is %s" % 'A')

print("this is {0}, {1}".format('A', 'B'))

# raw 表示原字符串
b = r'''
fdsfkjdkf
dfsdfds'
fsdf"
\n
'''
print(b)

# 成员判断
if "21" in a:
    print(True)
else:
    print(False)
