# division always returns a floating point number
print("8 / 5 = {0}".format(8 / 5))

# floor division discards the fractional part
print("8 // 5 = {0}".format(8 // 5))

# the % operator returns the remainder of the division
print("8 % 5 = {0}".format(8 % 5))

# 5 squared
print("5 ** 2 = {0}".format(5 ** 2))

